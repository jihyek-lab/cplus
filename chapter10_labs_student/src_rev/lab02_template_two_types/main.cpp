#include <iostream>
using namespace std;

/*
[Lab02] 다형 템플릿 (서로 다른 타입)
------------------------------------------------------------
문제 설명
1) 서로 다른 타입 배열을 복사하는 템플릿을 만든다.
2) 타입 변환이 들어가면 손실이 생길 수 있음을 확인한다.
------------------------------------------------------------
핵심 요약
- template<class T1, class T2>처럼 타입 변수를 여러 개 쓸 수 있다.
- 변환이 안전한지(정밀도/범위) 항상 고려해야 한다.
------------------------------------------------------------
주의 포인트
- static_cast로 변환해도 정보 손실 가능성은 남는다.
------------------------------------------------------------
*/

template <class T1, class T2>
void mcopy(T1 src[], T2 dest[], int n) {
    for(int i=0; i<n; i++)
        dest[i] = static_cast<T2>(src[i]);
}

int main() {
    int x[] = {1,2,3,4,5};
    double d[5];

    char c[5] = {'H','e','l','l','o'}, e[5];

    mcopy(x, d, 5);
    mcopy(c, e, 5);

    for(int i=0;i<5;i++) cout << d[i] << ' ';
    cout << "\n";
    for(int i=0;i<5;i++) cout << e[i] << ' ';
    cout << "\n";
}
