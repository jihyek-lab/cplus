#include <iostream>
using namespace std;

/*
[Lab05] 기본 생성자 자동 생성 / 자동 생성 안 되는 경우
------------------------------------------------------------
이 코드로 알아야 할 것(핵심 체크)
1) 생성자를 하나도 선언하지 않으면, 컴파일러가 기본 생성자 A()를 만들어줄 수 있다.
2) 생성자를 하나라도 선언하면, 기본 생성자(매개변수 없는 생성자)는 자동 생성되지 않는다.
   -> 이때 기본 생성자가 필요하면 직접 만들어야 한다.
------------------------------------------------------------
[실습 팁]
- 주석 처리된 줄을 풀어 컴파일 에러를 직접 확인하자.
*/

class A {
public:
    int x; // 생성자 없음 -> A() 자동 생성 가능
};

class B {
public:
    int x;
    B(int v) { x = v; } // 생성자 선언 -> B()는 자동 생성되지 않음
};

int main() {
    A a; // OK: 컴파일러가 A()를 만들어줄 수 있음
    cout << "A ok\n";

    B b1(10); // OK: B(int) 존재
    cout << "B(10) ok\n";

    //B b2; // (에러) B()가 없어서 기본 생성 불가

    return 0;
}
