#include <iostream>
using namespace std;

/*
[Lab02] 오버로딩 실패: 리턴 타입만 다르면 안 됨
-----------------------------------------
핵심
- 아래 두 함수는 이름도 같고, 매개변수도 완전히 동일하다.
- 호출 시점에 "어떤 함수를 부를지" 구분할 수 없으므로 컴파일 오류.

실습 방법
- 처음엔 컴파일 에러가 나야 정상이다.
- 학습 목적: "리턴 타입은 오버로딩 기준이 아니다"를 확실히 기억.
-----------------------------------------
*/

// (에러) 리턴 타입만 다르면 오버로딩 불가
int sum(int a, int b) { return a + b; }
double sum(int a, int b) { return (double)(a + b); }

int main() {
    cout << sum(2, 5) << "\n";
    return 0;
}
