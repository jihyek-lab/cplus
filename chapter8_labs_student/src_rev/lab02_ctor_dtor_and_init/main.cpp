#include <iostream>
#include <string>
using namespace std;

/*
[Lab02] 생성자/소멸자 순서 + 기본 생성자 문제 + 초기화 리스트
------------------------------------------------------------
문제 설명
1) 상속에서 생성/소멸 순서를 확인한다.
2) 기본 클래스에 기본 생성자가 없을 때 발생하는 문제를 이해한다.
3) 초기화 리스트로 부모 생성자를 명시적으로 호출한다.
------------------------------------------------------------
핵심 요약
- 생성: Base -> Derived, 소멸: Derived -> Base
- Base()가 없으면 파생 생성자에서 Base(...)를 명시해야 함
- 초기화 리스트는 "부모/멤버" 초기화의 표준 방식
------------------------------------------------------------
*/

class A {
public:
    // 강의 코멘트:
    // 기본 생성자 A()를 일부러 두지 않았다.
    // 그래서 파생 클래스는 어떤 A를 만들지 반드시 밝혀야 한다.
    A(int x) { cout << "Ctor A(int) " << x << "\n"; }
    ~A() { cout << "Dtor A\n"; }
};

class B : public A {
public:
    // 강의 코멘트:
    // 부모 생성자는 함수 본문 전에 먼저 호출되어야 하므로 초기화 리스트에서 지정한다.
    B(int x) : A(x + 3) { cout << "Ctor B(int) " << x << "\n"; }
    ~B() { cout << "Dtor B\n"; }
};

class C : public B {
public:
    C() : B(5) { cout << "Ctor C\n"; }
    ~C() { cout << "Dtor C\n"; }
};

int main() {
    // 강의 코멘트:
    // 객체 하나를 만들면 C -> B -> A 순서가 아니라,
    // "기반부터" A -> B -> C 순서로 생성된다.
    // 소멸은 반대로 C -> B -> A 이다.
    C c;
    return 0;
}

