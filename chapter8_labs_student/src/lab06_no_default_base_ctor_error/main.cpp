#include <iostream>
using namespace std;

/*
[Lab06] 기본 클래스에 기본 생성자 없으면? (컴파일 가능 버전)
------------------------------------------------------------
핵심
- 파생 클래스 생성자는 기본적으로 Base()를 호출하려 한다.
- 그런데 Base()가 없고 Base(int)만 있으면,
  파생 클래스에서 어떤 Base 생성자를 호출할지 "명시"해야 한다.

실습
- 원래 의도는 컴파일 에러 상황을 보여주는 것이다.
- 여기서는 파일 자체는 실행 가능하도록, 초기화 리스트로 수정한 버전을 둔다.
- 원래 에러 버전은 아래 주석 블록에 남겨 둔다.
------------------------------------------------------------
*/

class A {
public:
    A(int x) { cout << "Ctor A(int) " << x << "\n"; }
};

class B : public A {
public:
    B() : A(10) {
        // 강의 코멘트:
        // A()가 없으므로 어떤 A 생성자를 쓸지 직접 적어 주어야 한다.
        cout << "Ctor B\n";
    }
    B(int x) : A(x + 3) {
        cout << "Ctor B(int) " << x << "\n";
    }    
};

int main() {
    B b1;
    B b2(5);    
}

