#include <iostream>
using namespace std;

/*
[Lab07] 초기화 리스트로 기본 클래스 생성자 선택
------------------------------------------------------------
핵심
- 파생 클래스 생성자 뒤에 : A(...) 를 붙여서
  기본 클래스 생성자를 명시적으로 호출한다.
- 이게 없으면 기본적으로 A()를 찾는다.

여기서 확인할 것
- B(5) 생성 시 A(8)이 먼저 호출되고, 그 다음 B 생성자 본문 실행.
------------------------------------------------------------
*/

class A {
public:
    A(int x) { cout << "Ctor A(int) " << x << "\n"; }
};

class B : public A {
public:
    B(int x) : A(x + 3) {
        cout << "Ctor B(int) " << x << "\n";
    }
};

int main() {
    B b(5);
}
